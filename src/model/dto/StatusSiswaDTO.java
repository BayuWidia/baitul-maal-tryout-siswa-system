package model.dto;

public class StatusSiswaDTO {

	private String siap;
	private String belum_siap;
	private String butuh_bantuan;
	private String all_status_kinerja;

	public String getSiap() {
		return siap;
	}

	public void setSiap(String siap) {
		this.siap = siap;
	}

	public String getBelum_siap() {
		return belum_siap;
	}

	public void setBelum_siap(String belum_siap) {
		this.belum_siap = belum_siap;
	}

	public String getButuh_bantuan() {
		return butuh_bantuan;
	}

	public void setButuh_bantuan(String butuh_bantuan) {
		this.butuh_bantuan = butuh_bantuan;
	}

	public String getAll_status_kinerja() {
		return all_status_kinerja;
	}

	public void setAll_status_kinerja(String all_status_kinerja) {
		this.all_status_kinerja = all_status_kinerja;
	}

}
